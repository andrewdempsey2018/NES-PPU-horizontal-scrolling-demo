level_data: .res 2

.segment "CODE"

.proc irq_handler
  rti
.endproc

.proc nmi_handler
                        ; save registers
  php
  pha
  txa
  pha
  tya
  pha

  lda #$00
  sta OAMADDR
  lda #$02
  sta OAMDMA
  lda #$00

  inc scroll_x
  bne :+
  lda nametable_number
  eor #$01
  sta nametable_number
:

NewColumnCheck:
  lda scroll_x
  and #%00000111
  bne NewColumnCheckDone
  jsr LoadColumn
  
  lda column_number
  clc
  adc #1
  sta column_number
  cmp #32
  bne :+
  lda #0
  sta column_number
 :
NewColumnCheckDone:

  lda scroll_x
  sta PPUSCROLL         ; x scroll
  lda #$00
  sta PPUSCROLL         ; y scroll

                        ; This is the PPU clean up section, so rendering the next frame starts properly.
  lda #%10010000        ; enable NMI, sprites from Pattern Table 0, background from Pattern Table 1
  sta PPUCTRL
  lda #%00011110        ; enable sprites, enable background, no clipping on left side
  sta PPUMASK

  ;;
  lda #$00
  sta sleeping

                        ; restore registers and return
  pla
  tay
  pla
  tax
  pla
  plp

  rti
.endproc

.proc main
  ldx PPUSTATUS
  ldx #$3f
  stx PPUADDR
  ldx #$00
  stx PPUADDR

  lda #0
  sta PPUMASK

load_palettes:
  lda palettes,X
  sta PPUDATA
  inx
  cpx #$20              ; there are 32 colours to load
  bne load_palettes

  ;;;;;;;;;;;;;;;;;;;;;;;;;
  ;lda PPUSTATUS
  ;lda #$20
  ;sta PPUADDR
  ;lda #$00
  ;sta PPUADDR

  lda #>screen1
  sta level_data+1
  lda #<screen1
  sta level_data

InitializeNametables:
  lda #$01
  sta nametable_number
  lda #$00
  sta scroll_x
  sta column_number
InitializeNametablesLoop:
  jsr LoadColumn           ; draw bg column
  lda scroll_x          ; go to next column
  clc
  adc #$08
  sta scroll_x
  inc column_number
  lda column_number        ; repeat for first nametable 
  cmp #$20
  bne InitializeNametablesLoop
  
  lda #$00
  sta nametable_number
  sta scroll_x
  sta column_number
  jsr LoadColumn           ; draw first column of second nametable
  inc column_number
  
InitializeNametablesDone:
  
  ;;;;;;;;;;;;;;;;;;;;;;;;;

  cli

  lda #%10010000        ; turn on NMIs, sprites use first pattern table
  sta PPUCTRL
  lda #%00011110        ; turn on screen
  sta PPUMASK
  ;;

vblankwait:             ; wait for another vblank before continuing
  bit PPUSTATUS
  bpl vblankwait

mainloop:

  jsr read_controller

done:
  ;loop
  inc sleeping
sleep:
  lda sleeping
  bne sleep

  jmp mainloop
.endproc

