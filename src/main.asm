column_number: .res 1
scroll_x: .res 1

.segment "CODE"

.proc irq_handler
  rti
.endproc

.proc nmi_handler
                        ; save registers
  php
  pha
  txa
  pha
  tya
  pha

  lda #$00
  sta OAMADDR
  lda #$02
  sta OAMDMA
  lda #$00

